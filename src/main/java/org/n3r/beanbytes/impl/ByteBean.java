package org.n3r.beanbytes.impl;

public class ByteBean {
    private byte[] result;
    private int processBytesNum;
    private int size;

    public byte[] getResult() {
        return result;
    }

    public void setResult(byte[] result) {
        this.result = result;
    }

    public int getProcessBytesNum() {
        return processBytesNum;
    }

    public void setProcessBytesNum(int processBytesNum) {
        this.processBytesNum = processBytesNum;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
